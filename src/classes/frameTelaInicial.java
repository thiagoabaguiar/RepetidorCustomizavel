    private void btnContarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContarActionPerformed

        int inicioContador = sldInicioContador.getValue();
        int fimContador = sldFimContador.getValue();
        int passoContador = sldPassoContador.getValue();

        DefaultListModel lista = new DefaultListModel();

        for (int i = inicioContador; i <= fimContador; i += passoContador) {

            lista.addElement(i);

        }

        txtContagem.setModel(lista);

    }//GEN-LAST:event_btnContarActionPerformed

